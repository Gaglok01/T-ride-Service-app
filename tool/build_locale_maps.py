"""Generate translations_*.dart from embedded locale strings (same keys as _en)."""
from __future__ import annotations

import re
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
DART = ROOT / "lib/localization/app_translations.dart"


def parse_dart_string(s: str, start: int) -> tuple[str, int]:
    quote = s[start]
    assert quote in "'\""
    i = start + 1
    out: list[str] = []
    esc = {"n": "\n", "r": "\r", "t": "\t", "\\": "\\", '"': '"', "'": "'"}
    while i < len(s):
        c = s[i]
        if quote == '"' and c == "\\":
            i += 1
            if i < len(s):
                out.append(esc.get(s[i], s[i]))
                i += 1
            continue
        if c == quote:
            return "".join(out), i + 1
        out.append(c)
        i += 1
    raise ValueError("unterminated string")


def load_en_map() -> dict[str, str]:
    text = DART.read_text(encoding="utf-8")
    start = text.index("static const Map<String, String> _en = {") + len(
        "static const Map<String, String> _en = "
    )
    end = text.index("\n  static const Map<String, String> _ar", start)
    inner = text[start:end].strip()
    if inner.startswith("{"):
        inner = inner[1:]
    if inner.rstrip().endswith("}"):
        inner = inner.rstrip()[:-1]
    result: dict[str, str] = {}
    i = 0
    n = len(inner)
    while i < n:
        while i < n and inner[i] in " \t\n\r,":
            i += 1
        if i >= n or inner[i] == "}":
            break
        if inner[i : i + 2] == "//":
            while i < n and inner[i] != "\n":
                i += 1
            continue
        if inner[i] != "'":
            i += 1
            continue
        key, j = parse_dart_string(inner, i)
        i = j
        while i < n and inner[i] in " \t\n\r":
            i += 1
        if i >= n or inner[i] != ":":
            continue
        i += 1
        while i < n and inner[i] in " \t\n\r":
            i += 1
        if i >= n or inner[i] not in "'\"":
            continue
        val, j = parse_dart_string(inner, i)
        result[key] = val
        i = j
        while i < n and inner[i] in " \t\n\r,":
            i += 1
    return result


def dart_string_literal(s: str) -> str:
    """Emit a Dart string literal (prefer single quotes)."""
    if "\n" in s or "\r" in s:
        body = (
            s.replace("\\", "\\\\")
            .replace("\r", "\\r")
            .replace("\n", "\\n")
            .replace('"', '\\"')
        )
        return f'"{body}"'
    if "'" not in s:
        return f"'{s}'"
    if '"' not in s or s.count("'") <= s.count('"'):
        esc = s.replace("\\", "\\\\").replace("'", "\\'")
        return f"'{esc}'"
    body = s.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{body}"'


def emit_map(name: str, data: dict[str, str], en: dict[str, str]) -> str:
    lines = [
        "// Generated by tool/build_locale_maps.py — do not edit by hand.",
        f"const Map<String, String> {name} = {{",
    ]
    for key in en:
        if key not in data:
            raise KeyError(f"missing translation for {key!r}")
        lines.append(f"  {dart_string_literal(key)}: {dart_string_literal(data[key])},")
    lines.append("};")
    return "\n".join(lines) + "\n"


def main() -> None:
    en = load_en_map()
    # Spanish
    es = {k: _es[k] for k in en}
    # French
    fr = {k: _fr[k] for k in en}
    # Chinese (Simplified)
    zh = {k: _zh[k] for k in en}

    (ROOT / "lib/localization/translations_es.dart").write_text(
        emit_map("kAppTranslationsEs", es, en), encoding="utf-8"
    )
    (ROOT / "lib/localization/translations_fr.dart").write_text(
        emit_map("kAppTranslationsFr", fr, en), encoding="utf-8"
    )
    (ROOT / "lib/localization/translations_zh.dart").write_text(
        emit_map("kAppTranslationsZh", zh, en), encoding="utf-8"
    )
    print("Wrote translations_es.dart, translations_fr.dart, translations_zh.dart")


